package _02_operators;

public class DecrementEncrement {

    public static void main(String[] args) {

        int number1=10;
        int number2=10;

        System.out.println(number1++); //postfix dir.Önce değeri yazdırır daha sonra işlem yapar.


        System.out.println(++number2); //prefix dir.Önce değeri arttırır işlem yapar daha sonra değeri yazdırır.
    }
}
